<center><h2>Information about RelayAwards</h2></br>
<h4>Rules</h4>
The rules are pretty simple: there's a <a href="/awards.php">list of awards</a> that each relay will earn if it reaches the requirements.<p>
Some awards are easy to earn because they only require the operator to follow best practices.
Some awards will take time and a lot of effort to earn.
There are even awards that are almost impossible to reach and if actually do reach them you will earn the glorious crowne!<p>
</br></br>
<h4>About the code</h4>
RelayAwards uses the onionoo API for fetching statistics from the Tor-network. The code is written in PHP and uses bootstrap as template.
